<template>
    <BaseModal :modal="modal">
        <template #header>
            <div>
                {{ __(isFolder ? "Rename folder" : "Rename file") }}
            </div>
        </template>

        <template v-if="isFolder">
            <div class="p-6">
                <input
                    ref="input"
                    type="text"
                    class="appearance-none w-full bg-white dark:bg-gray-800 border border-gray-300 dark:border-gray-800 h-10 px-3 rounded-lg"
                    :placeholder="name"
                    :value="name"
                    autofocus
                    required
                />
            </div>
        </template>

        <template v-else>
            <div class="p-6">
                <div class="flex w-full relative">
                    <input
                        ref="input"
                        type="text"
                        class="appearance-none w-full bg-white dark:bg-gray-800 border border-gray-300 dark:border-gray-800 h-10 px-3 rounded-lg"
                        :placeholder="nameWithoutExtension"
                        :value="nameWithoutExtension"
                        autofocus
                        required
                    />

                    <span
                        class="flex items-center leading-normal bg-gray-50 dark:bg-gray-900 ml-3 rounded-lg px-3 whitespace-no-wrap text-grey-dark text-sm"
                    >
                        {{ extension }}
                    </span>
                </div>

                <p class="my-2 text-red-500" v-if="error">
                    {{ error }}
                </p>
            </div>
        </template>

        <template #footer>
            <Button theme="gray" class="mr-3" @click.prevent="close">
                {{ __("Cancel") }}
            </Button>
            <Button
                type="submit"
                :disabled="loading"
                :loading="loading"
                @click.prevent="confirmRename"
            >
                <template v-if="loading">
                    {{ __("Renaming") }}
                </template>
                <template v-else>
                    {{ __("Rename") }}
                </template>
            </Button>
        </template>
    </BaseModal>
</template>

<script setup>
import { ref, computed } from "vue";
import Button from "../Elements/Button.vue";
import BaseModal from "./BaseModal.vue";
import useBrowserStore from "../../stores/browser";

const store = useBrowserStore();

const props = defineProps({
    modal: {
        type: Object,
        required: true,
    },
});

const subject = computed(() => props.modal.payload);
const isFolder = computed(() => subject.value.type === "folder");
const name = computed(() => subject.value.name.replace(/^.*[\\/]/, ""));
const extension = computed(() => {
    if (!isFolder.value) {
        var re = /(?:\.([^.]+))?$/;

        let ext = re.exec(name.value);

        if (ext) {
            return ext[0];
        }
    }

    return "";
});

const nameWithoutExtension = computed(() =>
    name.value.replace(extension.value, "")
);

const error = ref();
const input = ref();
const loading = ref(false);

function close() {
    store.closeModal(props.modal.id);

    error.value = null;
}

function confirmRename() {
    const name = input.value.value;

    if (!name) {
        error.value = "The name is required";

        return false;
    }

    let nameToSave = isFolder.value ? name : name + extension.value;

    if (!nameToSave) {
        error.value = "The name is required";

        return false;
    }

    const newPath = (
        subject.value.path.substring(0, subject.value.path.lastIndexOf("/")) +
        "/" +
        nameToSave
    ).replace(/^\/+/, "");

    return (
        isFolder.value
            ? store.renameFolder(subject.value.path, newPath)
            : store.renameFile(subject.value.path, newPath)
    )
        .then((r) =>
            processResponse(r.response && r.response.data ? r.response.data : r)
        )
        .catch((r) =>
            processResponse(r.response && r.response.data ? r.response.data : r)
        );
}

function processResponse(result) {
    error.value =
        !result.errors || result.errors.length <= 0 ? null : result.message;
}
</script>
